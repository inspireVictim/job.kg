def _ensure_python_docx() -> None:
    try:
        importlib.import_module("docx")
    except ImportError:
        print(">>> Библиотека python-docx не найдена, выполняется установка...")
        subprocess.check_call([sys.executable, "-m", "pip", "install", "python-docx"])
        print(">>> Установка завершена.")


_ensure_python_docx()

from docx import Document
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING
from docx.oxml.ns import qn
from docx.oxml import OxmlElement
from docx.shared import Cm, Mm, Pt


# ────────────────────────── параметры оформления ──────────────────────────
FONT_NAME = "Times New Roman"
FONT_SIZE_PT = 14
LINE_SPACING = 1.5
FIRST_LINE_INDENT_CM = 1.25
LEFT_MARGIN_MM = 30
RIGHT_MARGIN_MM = 15
TOP_MARGIN_MM = 20
BOTTOM_MARGIN_MM = 20

OUTPUT_FILENAME = "ПЗ_Корпоративный_Портал.docx"


# ────────────────────────── вспомогательные функции ──────────────────────────
def _set_run_default(run) -> None:
    run.font.name = FONT_NAME
    run.font.size = Pt(FONT_SIZE_PT)
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = OxmlElement("w:rFonts")
        rPr.append(rFonts)
    rFonts.set(qn("w:ascii"), FONT_NAME)
    rFonts.set(qn("w:hAnsi"), FONT_NAME)
    rFonts.set(qn("w:cs"), FONT_NAME)
    rFonts.set(qn("w:eastAsia"), FONT_NAME)


def _apply_paragraph_format(paragraph, *, first_line_indent: bool = True,
                             alignment=WD_ALIGN_PARAGRAPH.JUSTIFY) -> None:
    pf = paragraph.paragraph_format
    pf.line_spacing_rule = WD_LINE_SPACING.MULTIPLE
    pf.line_spacing = LINE_SPACING
    pf.first_line_indent = Cm(FIRST_LINE_INDENT_CM) if first_line_indent else Cm(0)
    pf.space_before = Pt(0)
    pf.space_after = Pt(0)
    paragraph.alignment = alignment


def add_normal(document: Document, text: str) -> None:
    """Добавление обычного абзаца основного текста с красной строкой."""
    p = document.add_paragraph()
    _apply_paragraph_format(p, first_line_indent=True, alignment=WD_ALIGN_PARAGRAPH.JUSTIFY)
    run = p.add_run(text)
    _set_run_default(run)


def add_heading_centered(document: Document, text: str, *, bold: bool = True,
                         size: int = FONT_SIZE_PT, page_break_before: bool = False) -> None:
    """Заголовок верхнего уровня (по центру, прописные буквы)."""
    p = document.add_paragraph()
    _apply_paragraph_format(p, first_line_indent=False, alignment=WD_ALIGN_PARAGRAPH.CENTER)
    if page_break_before:
        p.paragraph_format.page_break_before = True
    run = p.add_run(text)
    run.bold = bold
    _set_run_default(run)
    run.font.size = Pt(size)


def add_heading_left(document: Document, text: str, *, bold: bool = True) -> None:
    """Заголовок подраздела (с красной строкой, по ширине)."""
    p = document.add_paragraph()
    _apply_paragraph_format(p, first_line_indent=True, alignment=WD_ALIGN_PARAGRAPH.LEFT)
    run = p.add_run(text)
    run.bold = bold
    _set_run_default(run)


def add_list_item(document: Document, text: str) -> None:
    """Элемент маркированного перечисления (с тире), без красной строки, с выравниванием по ширине."""
    p = document.add_paragraph()
    pf = p.paragraph_format
    pf.line_spacing_rule = WD_LINE_SPACING.MULTIPLE
    pf.line_spacing = LINE_SPACING
    pf.left_indent = Cm(1.25)
    pf.first_line_indent = Cm(-0.5)
    pf.space_before = Pt(0)
    pf.space_after = Pt(0)
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    run = p.add_run("— " + text)
    _set_run_default(run)


def add_blank(document: Document) -> None:
    p = document.add_paragraph()
    _apply_paragraph_format(p, first_line_indent=False)
    run = p.add_run("")
    _set_run_default(run)


def add_title_line(document: Document, text: str, *, bold: bool = False, size: int = FONT_SIZE_PT,
                   alignment=WD_ALIGN_PARAGRAPH.CENTER) -> None:
    p = document.add_paragraph()
    _apply_paragraph_format(p, first_line_indent=False, alignment=alignment)
    run = p.add_run(text)
    run.bold = bold
    _set_run_default(run)
    run.font.size = Pt(size)


# ────────────────────────── построение документа ──────────────────────────
def build_document() -> Document:
    document = Document()

    # глобальные параметры стиля Normal
    style = document.styles["Normal"]
    style.font.name = FONT_NAME
    style.font.size = Pt(FONT_SIZE_PT)
    rPr = style.element.get_or_add_rPr()
    rFonts = rPr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = OxmlElement("w:rFonts")
        rPr.append(rFonts)
    rFonts.set(qn("w:ascii"), FONT_NAME)
    rFonts.set(qn("w:hAnsi"), FONT_NAME)
    rFonts.set(qn("w:cs"), FONT_NAME)
    rFonts.set(qn("w:eastAsia"), FONT_NAME)
    style.paragraph_format.line_spacing_rule = WD_LINE_SPACING.MULTIPLE
    style.paragraph_format.line_spacing = LINE_SPACING

    # поля
    section = document.sections[0]
    section.left_margin = Mm(LEFT_MARGIN_MM)
    section.right_margin = Mm(RIGHT_MARGIN_MM)
    section.top_margin = Mm(TOP_MARGIN_MM)
    section.bottom_margin = Mm(BOTTOM_MARGIN_MM)

    _build_title_page(document)
    _build_contents(document)
    _build_introduction(document)
    _build_chapter_1(document)
    _build_chapter_2(document)
    _build_chapter_3(document)
    _build_conclusion(document)
    _build_references(document)

    return document


